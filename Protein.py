def parse_swissprot_gene(title: str) -> str:
    """
    Extract the gene name from a SwissProt BLAST hit title.

    SwissProt format:
        sp|P12345|GENE_SPECIES Description OS=... GN=GeneName PE=... SV=...

    Strategy:
      1. Try the explicit  GN=  field  (most reliable)
      2. Fall back to the entry-name segment before the underscore
         e.g.  sp|P12345|BRCA1_HUMAN  →  BRCA1
    """
    # ── Strategy 1: GN= field ───────────────────────────────────────────
    gn_match = re.search(r'\bGN=(\S+)', title)
    if gn_match:
        return gn_match.group(1)

    # ── Strategy 2: entry name  sp|ACC|ENTRY_SPECIES ────────────────────
    entry_match = re.match(r'sp\|[^|]+\|([^_\s]+)', title)
    if entry_match:
        return entry_match.group(1)

    return 'N/A'

def clean_sequence(raw: str) -> str:
    """Strip whitespace and uppercase the DNA string."""
    return raw.replace('\n', '').replace(' ', '').upper()


def get_reverse_complement(dna: str) -> str:
    """Return the reverse complement of a DNA sequence."""
    complement = {'A': 'T', 'C': 'G', 'G': 'C', 'T': 'A'}
    return "".join(complement.get(base, base) for base in reversed(dna))


def translate_orf(dna: str, start: int) -> Tuple[List[str], bool]:
    """
    Translate a DNA sequence from 'start' into a list of amino acid short names.

    Returns
    -------
    protein   : list of three-letter amino acid names (e.g. ['Met', 'Ala', ...])
    has_stop  : True if translation ended with a stop codon (complete ORF),
                False if it ran off the end of the sequence (incomplete ORF)
    """
    protein: List[str] = []
    has_stop = False

    for i in range(start, len(dna) - 2, 3):
        codon = dna[i:i + 3]
        aa = SHORT_NAME.get(codon, None)

        if aa is None:
            continue

        if aa == 'Stop':
            has_stop = True
            break

        protein.append(aa)

    return protein, has_stop


def find_orfs(
    dna: str,
    strand: str,
    original_length: int,
    min_length: int = MIN_ORF_LENGTH,
) -> List[Dict]:
    """
    Scan all three reading frames of dna and return ORFs that are at least
    min_length amino acids long AND have a confirmed stop codon.
    Deduplicates by stop-codon position, keeping only the longest ORF per stop.
    """
    orfs: List[Dict] = []

    for frame_idx in range(3):
        best_per_stop: Dict[int, Dict] = {}

        i = frame_idx
        while i <= len(dna) - 3:
            if dna[i:i + 3] == 'ATG':
                protein, has_stop = translate_orf(dna, i)

                if not has_stop:
                    i += 3
                    continue

                if len(protein) >= min_length:
                    stop_pos = i + len(protein) * 3

                    one_letter    = ''.join(ONE_LETTER.get(aa, '') for aa in protein)
                    display_pos   = (i + 1) if strand == '+' else (original_length - i)
                    display_frame = (frame_idx + 1) if strand == '+' else -(frame_idx + 1)

                    candidate = {
                        'frame'     : display_frame,
                        'start'     : display_pos,
                        'length'    : len(protein),
                        'protein'   : protein,
                        'one_letter': one_letter,
                        'stop_pos'  : stop_pos,
                    }

                    if (stop_pos not in best_per_stop or
                            len(protein) > best_per_stop[stop_pos]['length']):
                        best_per_stop[stop_pos] = candidate

            i += 3

        orfs.extend(best_per_stop.values())

    return orfs


def blast_protein_local(one_letter_seq: str, orf_id: int) -> Optional[Dict]:
    """
    Runs a LOCAL BLASTp search. 
    Requires 'blastp' to be installed and the 'swissprot' database indexed.
    """
    # 1. Save the ORF to a temporary file
    temp_query = f"temp_orf_{orf_id}.fasta"
    with open(temp_query, "w") as f:
        f.write(f">ORF_{orf_id}\n{one_letter_seq}\n")

    # 2. Run the local blastp command
    # Replace 'path/to/swissprot' with the actual path to the indexed files
    db_path = "swissprot" 
    
    try:
        # We call the blastp program directly
        result = subprocess.run([
            "blastp",
            "-query", temp_query,
            "-db", db_path,
            "-outfmt", "5",      # Format 5 is XML
            "-max_target_seqs", "1"
        ], capture_output=True, text=True)

        # 3. Parse the XML results
        blast_records = NCBIXML.parse(io.StringIO(result.stdout))
        
        for record in blast_records:
            if record.alignments:
                best_algn = record.alignments[0]
                best_hsp = best_algn.hsps[0]
                
                # Cleanup temp file
                if os.path.exists(temp_query): os.remove(temp_query)

                return {
                    'orf_id': orf_id,
                    'gene_name': parse_swissprot_gene(best_algn.title),
                    'name': best_algn.title,
                    'identity': round((best_hsp.identities / best_hsp.align_length) * 100, 1),
                    'e_value': f"{best_hsp.expect:.2e}",
                    'score': best_hsp.score,
                }
                
    except Exception as e:
        print(f"Local BLAST Error: {e}")
    
    # Cleanup temp file if failed
    if os.path.exists(temp_query): os.remove(temp_query)
    return None

